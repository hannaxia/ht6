/** Ray-casting point-in-polygon test. Coordinates as [lng, lat]. */
export function isInLakeOntario(lng: number, lat: number): boolean {
  let inside = false;
  const poly = LAKE_ONTARIO_BOUNDARY;
  for (let i = 0, j = poly.length - 1; i < poly.length; j = i++) {
    const [xi, yi] = poly[i]!;
    const [xj, yj] = poly[j]!;
    const intersects =
      yi > lat !== yj > lat && lng < ((xj - xi) * (lat - yi)) / (yj - yi) + xi;
    if (intersects) inside = !inside;
  }
  return inside;
}
